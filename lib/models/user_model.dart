class UserModel {
  // instance variables
  final String id;
  final String name;
  final String email;
  final String dob;
  final String yearGroup;
  final String major;
  final String residence;
  final String favFood;
  final String favMovie;

  UserModel({
    required this.id,
    required this.name,
    required this.email,
    required this.dob,
    required this.yearGroup,
    required this.major,
    required this.residence,
    required this.favFood,
    required this.favMovie,
  });
}
